"""
Prepare dataset: pool all split data, stratified re-split 70/20/10,
then write data.yaml with absolute paths.

Run once before training:
    python ml/scripts/prepare_dataset.py
"""

import shutil
import random
import collections
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]   # e:\Barka\Kartaloka

SPLIT_RATIO = {"train": 0.70, "valid": 0.20, "test": 0.10}
SEED = 42

OUT_SPLITS = ["train", "valid", "test"]


def collect_all():
    """Gather every (image_path, label_path, primary_class) from all splits.
    Prefers *_orig backup dirs if they exist (idempotent re-runs)."""
    samples = []
    seen = set()
    # look in _orig backups first, fall back to live dirs
    candidates = []
    for split in ["train", "valid", "test"]:
        backup = ROOT / (split + "_orig")
        live   = ROOT / split
        candidates.append(backup if backup.exists() else live)

    for src_dir in candidates:
        img_dir = src_dir / "images"
        lbl_dir = src_dir / "labels"
        if not img_dir.exists():
            continue
        for img in sorted(img_dir.glob("*.jpg")) + sorted(img_dir.glob("*.png")):
            if img.stem in seen:
                continue
            seen.add(img.stem)
            lbl = lbl_dir / (img.stem + ".txt")
            if not lbl.exists():
                continue
            # primary class = first annotation line
            first_line = lbl.read_text().strip().splitlines()
            if not first_line:
                continue
            cls = int(first_line[0].split()[0])
            samples.append((img, lbl, cls))
    return samples


def stratified_split(samples):
    """Split per-class so each class is proportionally represented."""
    random.seed(SEED)

    by_class = collections.defaultdict(list)
    for s in samples:
        by_class[s[2]].append(s)

    splits = {"train": [], "valid": [], "test": []}
    for cls, items in by_class.items():
        random.shuffle(items)
        n = len(items)
        n_valid = max(1, int(n * SPLIT_RATIO["valid"]))
        n_test  = max(1, int(n * SPLIT_RATIO["test"]))
        n_train = n - n_valid - n_test

        splits["train"].extend(items[:n_train])
        splits["valid"].extend(items[n_train:n_train + n_valid])
        splits["test"].extend(items[n_train + n_valid:])

    return splits


def write_splits(splits):
    # Backup originals to _orig if not already done
    for split in OUT_SPLITS:
        orig = ROOT / split
        backup = ROOT / (split + "_orig")
        if orig.exists() and not backup.exists():
            shutil.copytree(orig, backup)
            print(f"[prepare] Backed up {split}/ -> {split}_orig/")

    # Clear and rewrite
    for split in OUT_SPLITS:
        img_out = ROOT / split / "images"
        lbl_out = ROOT / split / "labels"
        # clear
        if img_out.exists(): shutil.rmtree(img_out)
        if lbl_out.exists(): shutil.rmtree(lbl_out)
        img_out.mkdir(parents=True)
        lbl_out.mkdir(parents=True)

        for img, lbl, _ in splits[split]:
            shutil.copy2(img, img_out / img.name)
            shutil.copy2(lbl, lbl_out / lbl.name)

        print(f"[prepare] {split}: {len(splits[split])} images")


def write_yaml():
    content = f"""# Kartaloka — Directional Arrows dataset
# Generated by ml/scripts/prepare_dataset.py

path: {ROOT.as_posix()}
train: train/images
val:   valid/images
test:  test/images

nc: 4
names:
  0: down
  1: left
  2: right
  3: up
"""
    (ROOT / "data.yaml").write_text(content)
    print(f"[prepare] Wrote data.yaml")


if __name__ == "__main__":
    samples = collect_all()
    print(f"[prepare] Total samples found: {len(samples)}")

    cls_count = collections.Counter(s[2] for s in samples)
    names = {0: "down", 1: "left", 2: "right", 3: "up"}
    print(f"[prepare] Classes: { {names[k]: v for k, v in sorted(cls_count.items())} }")

    splits = stratified_split(samples)
    write_splits(splits)
    write_yaml()
    print("[prepare] Done. Re-run train.py to retrain with balanced splits.")
